def find_triple(nums, check_list):
    for i in range(len(nums)):
        for j in range(len(nums)-1):
            for k in range(len(nums)-2):
                check_sum = nums[i] + nums[j+1] + nums[k+2]
                if check_sum == 0 and nums[i] not in check_list and nums[j+1] not in check_list and nums[k+2] not in check_list:
                    triple = (i, j+1, k+2)
                    return triple

def trips_list(in_list): 
    out_list = []
    check_list = []

    while len(check_list) < len(in_list):
        indices = find_triple(in_list, check_list)
        if indices is not None:
            new_trip = (in_list[indices[0]], in_list[indices[1]], in_list[indices[2]])
            out_list.append(new_trip)
            check_list.append(new_trip[0])
            check_list.append(new_trip[1])
            check_list.append(new_trip[2])
    return out_list

nums = [-15,-11,-7,-3,1,2,3,4,5,6,7,8]

trips_list(in_list=nums)
